base_css_path <- file.path(project_root, "styles-base.css")
ai_css_path <- file.path(project_root, "styles-ai.css")
override_css_path <- file.path(app_dir, "generated", "style-overrides.css")

read_text_file <- function(path) {
  paste(readLines(path, warn = FALSE, encoding = "UTF-8"), collapse = "\n")
}

write_override_css <- function(css_text) {
  writeLines(css_text, override_css_path, useBytes = TRUE)
}

escape_attr <- function(x) {
  gsub('"', "&quot;", x, fixed = TRUE)
}

