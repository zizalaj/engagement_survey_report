server <- function(input, output, session) {
  defaults <- list(
    accent = "#63e8c6",
    accent_light = "#e1faf4",
    accent_pink = "#ff8ade",
    accent_pink_strong = "#ff67aa",
    yellow = "#ffd06b",
    yellow_light = "#ffedc3",
    light_grey = "#d1d1d1",
    normal_grey = "#a5a5a5",
    page_bg = "#ffffff",
    black = "#000000",
    intro_badge_size = 36,
    intro_main_size = 160,
    intro_subtitle_size = 64,
    intro_logo_left = 48,
    intro_logo_bottom = 40,
    agenda_padding_y = 140,
    agenda_padding_x = 180,
    agenda_title_size = 80,
    agenda_list_gap = 30,
    agenda_item_gap = 36
  )

  observeEvent(input$reset_defaults, {
    for (nm in names(defaults)) {
      value <- defaults[[nm]]
      if (is.numeric(value)) {
        updateSliderInput(session, nm, value = value)
      } else {
        updateTextInput(session, nm, value = value)
      }
    }
  })

  override_css <- reactive({
    build_override_css(reactiveValuesToList(input))
  })

  observe({
    write_override_css(override_css())
  })

  output$preview_ui <- renderUI({
    tags$div(
      tags$style(HTML(base_css)),
      tags$style(HTML(ai_css)),
      tags$style(HTML(override_css())),
      HTML(preview_markup)
    )
  })

  output$css_text <- renderText({
    override_css()
  })
}

shinyApp(ui, server)
